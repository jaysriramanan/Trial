package com.example.test.trial;

public class SpinnerItem {
    private String name;
    private int imageId;

    public SpinnerItem(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}
